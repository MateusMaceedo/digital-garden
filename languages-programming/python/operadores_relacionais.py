### Operadores Relacionais

## Operadores de comparação

## igua a
a = 1
b = 1
r = a == b
print(r)

## diferente de
a = 1
b = 2
r = a != b
print(r)

## maior que
a = 3
b = 2
r = a > b
print(r)

## menor que
a = 1
b = 2
r = a < b
print(r)

## maior ou igual a
a = 3
b = 2
r = a >= b
print(r)

## menor ou igual a
a = 1
b = 2
r = a <= b
print(r)
